#ifndef E65_TEST_INTERFACE_PROCESSOR_TYPE_H_
#define E65_TEST_INTERFACE_PROCESSOR_TYPE_H_

#include "../../../include/type/exception.h"

namespace e65 {

	namespace test {

		namespace interface {

			#define E65_TEST_INTERFACE_PROCESSOR_HEADER "[E65::TEST::INTERFACE::PROCESSOR]"
#ifndef NDEBUG
			#define E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_HEADER E65_TEST_INTERFACE_PROCESSOR_HEADER " "
#else
			#define E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_HEADER
#endif // NDEBUG

			enum {
				E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_NONE = 0,
			};

			#define E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_MAX E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_NONE

			static const std::string E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_STR[] = {
				E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_HEADER "",
				};

			#define E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_STRING(_TYPE_) \
				(((_TYPE_) > E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_MAX) ? E65_STRING_UNKNOWN : \
					E65_STRING_CHECK(E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_STR[_TYPE_]))

			#define THROW_E65_TEST_INTERFACE_PROCESSOR_EXCEPTION(_EXCEPT_) \
				THROW_E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_FOMAT(_EXCEPT_, "", "")
			#define THROW_E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_FOMAT(_EXCEPT_, _FORMAT_, ...) \
				E65_EXCEPTION_THROW_FORMAT(E65_TEST_INTERFACE_PROCESSOR_EXCEPTION_STRING(_EXCEPT_), _FORMAT_, __VA_ARGS__)

			#define E65_TEST_INTERFACE_PROCESSOR_ACCUMULATOR_VALUE 0x00
			#define E65_TEST_INTERFACE_PROCESSOR_CYCLE_VALUE 7
			#define E65_TEST_INTERFACE_PROCESSOR_CYCLE_LAST_VALUE 0
			#define E65_TEST_INTERFACE_PROCESSOR_FLAGS_VALUE \
				(E65_PFLAG(e65::type::E65_PFLAG_BREAKPOINT) | E65_PFLAG(e65::type::E65_PFLAG_IRQ_DISABLE) \
					| E65_PFLAG(e65::type::E65_PFLAG_UNUSED))
			#define E65_TEST_INTERFACE_PROCESSOR_INDEX_X_VALUE 0x00
			#define E65_TEST_INTERFACE_PROCESSOR_INDEX_Y_VALUE 0x00
			#define E65_TEST_INTERFACE_PROCESSOR_RESET_ADDRESS 0xfffc
			#define E65_TEST_INTERFACE_PROCESSOR_STACK_POINTER_VALUE 0xfd
			#define E65_TEST_INTERFACE_PROCESSOR_STOP_VALUE false
			#define E65_TEST_INTERFACE_PROCESSOR_WAIT_VALUE false

			enum {
				E65_TEST_INTERFACE_PROCESSOR_ACCUMULATOR = 0,
				E65_TEST_INTERFACE_PROCESSOR_CYCLE,
				E65_TEST_INTERFACE_PROCESSOR_CYCLE_LAST,
				E65_TEST_INTERFACE_PROCESSOR_FLAGS,
				E65_TEST_INTERFACE_PROCESSOR_INDEX_X,
				E65_TEST_INTERFACE_PROCESSOR_INDEX_Y,
				E65_TEST_INTERFACE_PROCESSOR_INTERRUPT,
				E65_TEST_INTERFACE_PROCESSOR_PROGRAM_COUNTER,
				E65_TEST_INTERFACE_PROCESSOR_RESET,
				E65_TEST_INTERFACE_PROCESSOR_STACK_POINTER,
				E65_TEST_INTERFACE_PROCESSOR_STOP,
				E65_TEST_INTERFACE_PROCESSOR_WAIT,
			};

			#define E65_TEST_INTERFACE_PROCESSOR_MAX E65_TEST_INTERFACE_PROCESSOR_WAIT

			static const std::string E65_TEST_INTERFACE_PROCESSOR_STR[] = {
				"Accumulator",
				"Cycle",
				"Cycle-Last",
				"Flags",
				"Index-X",
				"Index-Y",
				"Interrupt",
				"Program-Counter",
				"Reset",
				"Stack-Pointer",
				"Stop",
				"Wait",
				};

			#define E65_TEST_INTERFACE_PROCESSOR_STRING(_TYPE_) \
				(((_TYPE_) > E65_TEST_INTERFACE_PROCESSOR_MAX) ? E65_STRING_UNKNOWN : \
					E65_STRING_CHECK(E65_TEST_INTERFACE_PROCESSOR_STR[_TYPE_]))
		}
	}
}

#endif // E65_TEST_INTERFACE_PROCESSOR_TYPE_H_
